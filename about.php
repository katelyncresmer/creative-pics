<h1>About Creative Pics</h1>
</header>
<p>Creative Pics is a social network for creative people to show off their artwork. Rather it be a poem, a sketch, a doodle, a photograph or a colored page from a coloring book, Creative Pics challenges everyone to create something artsy every day. And, then share it for others to enjoy and comment on it. So, fire up Photoshop, get out that sketchbook or take the camera out for a walk. Do something every day to get those creative juices flowing.</p>
<h2 id="abouth2">Ran Out of Ideas?</h2>
<ul>Here's a few things to get you started:
<li>Take a picture of your lastest poem</li>
<li>Create a masterpiece using only dry erase markers on a whiteboard</li>
<li>Edit an image in Photoshop and make textures from its brushes</li>
<li>Create something beautiful with chalk</li>
<li>And, check out what others are doing by signing up and checking out the live feed!</li>
</ul>
